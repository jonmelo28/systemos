    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
         int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja Excluir a situação","Atenção",JOptionPane.YES_NO_OPTION);
        
         if(confirma == JOptionPane.YES_OPTION){
         Situacao obj = new Situacao();
         
         obj.setIdsit(Integer.parseInt(txtIdSit.getText()));
         try {
              SituacaoDAO dao = new SituacaoDAO();   
              dao.deleteSituacao(obj);
              
              listarStiuacao();
              limpaCampos();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            
         }
    }//GEN-LAST:event_btnDelActionPerformed


