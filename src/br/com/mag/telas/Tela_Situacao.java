    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Situacao obj = new Situacao();
        obj.setSituacao(txtSit.getText());
        try {
              SituacaoDAO dao = new SituacaoDAO();   
              dao.cadastrarSituacao(obj);
               
                listarStiuacao();
                limpaCampos();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
           
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpActionPerformed
        // TODO add your handling code here:
         int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja atualizar a situação","Atenção",JOptionPane.YES_NO_OPTION);
        
         if(confirma == JOptionPane.YES_OPTION){
         Situacao obj = new Situacao();
         
         obj.setSituacao(txtSit.getText());
         obj.setIdsit(Integer.parseInt(txtIdSit.getText()));
         try {
              SituacaoDAO dao = new SituacaoDAO();   
              dao.editarSituacao(obj);
              
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            
            listarStiuacao();
            limpaCampos();
         }
    }//GEN-LAST:event_btnUpActionPerformed

