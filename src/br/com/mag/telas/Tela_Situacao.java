    public void listarStiuacao(){
         try {
            SituacaoDAO dao = new SituacaoDAO();
            List<Situacao> lista = dao.listsituacao();
            
                DefaultTableModel modelo = (DefaultTableModel)tbSituacao.getModel();
                modelo.setNumRows(0);
                
                for(Situacao s: lista){
                   modelo.addRow(new Object[]{
                       s.getIdsit(), 
                       s.getSituacao()
                   });
                    
                }
            
        } catch (Exception e) {
        }
    }
    
    public void limpaCampos(){
        txtIdSit.setText(null);
        txtSit.setText(null);
        
        btnDel.setEnabled(false);
        btnUp.setEnabled(false);
        btnAdd.setEnabled(true);
    }
    
     public void setar_campo(){
        int setar = tbSituacao.getSelectedRow();
        txtIdSit.setText(tbSituacao.getModel().getValueAt(setar, 0).toString());
        txtSit.setText(tbSituacao.getModel().getValueAt(setar, 1).toString());
        btnAdd.setEnabled(false);
        btnUp.setEnabled(true);
        btnDel.setEnabled(true);
    }

